USE library_system
GO

CREATE TABLE publisher
(
Name varchar(100) NOT NULL PRIMARY KEY,
Address varchar(255) NOT NULL,
Phone varchar(50) NOT NULL
);
GO

CREATE TABLE book
(
BookId int IDENTITY(1,1) PRIMARY KEY,
Title varchar(255) NOT NULL,
PublisherName varchar(100) FOREIGN KEY REFERENCES publisher(Name)
);
GO

CREATE TABLE book_authors
(
BookId int FOREIGN KEY REFERENCES book(BookId),
AuthorName varchar(150) NOT NULL
);
GO

CREATE TABLE borrower
(
CardNo int IDENTITY(1000,1) PRIMARY KEY,
Name varchar(100) NOT NULL,
Address varchar(255) NOT NULL,
Phone varchar(50) NOT NULL
);
GO

CREATE TABLE library_branch
(
BranchId int IDENTITY(1,1) PRIMARY KEY,
BranchName varchar(150) NOT NULL, 
Address varchar(255) NOT NULL
);
GO

CREATE TABLE book_copies
(
BookId int FOREIGN KEY REFERENCES book(BookId),
BranchId int FOREIGN KEY REFERENCES library_branch(BranchId),
NoOfCopies int NOT NULL
);
GO

CREATE TABLE book_loans
(
BookId int FOREIGN KEY REFERENCES book(BookId),
BranchId int FOREIGN KEY REFERENCES library_branch(BranchId),
CardNo int FOREIGN KEY REFERENCES borrower(CardNo),
DateOut varchar(8) NOT NULL,
DueDate varchar(8) NOT NULL
);
GO

